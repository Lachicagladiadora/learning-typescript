// OPERATORS  -> symbols that allow you to perform operations
// arithmetic operators ... 
console.log(1+1);
console.log(1-5);
console.log(9*4);
console.log(16/4);
// ... or comparative operators ...
console.log(4 == 4);
console.log(4 < 5);
console.log(4 >= 5);
// ... or logical operators 
console.log(true && true)
console.log(true && false)
console.log(false && true)
console.log(false && false)

console.log(true || true)
console.log(true || false)
console.log(false || true)
console.log(false || false)
